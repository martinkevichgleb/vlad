#include <iostream>
#include <cmath> // Для функции sqrt

int main() {
    // Дано
    double m1 = 8.0; // Средняя квадратическая погрешность первого угла (в секундах)
    double m2 = 9.0; // Средняя квадратическая погрешность второго угла (в секундах)
    double m3 = 5.0; // Средняя квадратическая погрешность третьего угла (в секундах)

    // Найти: среднюю квадратическую погрешность четвертого угла m4
    double m4 = std::sqrt(std::pow(m1, 2) + std::pow(m2, 2) + std::pow(m3, 2));

    // Вывод результата
    std::cout << "Средняя квадратическая погрешность четвертого угла: "
              << m4 << "″" << std::endl;

    return 0;
}
